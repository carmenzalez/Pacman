package pacman;

public class Coco {

    private int x;
    private int y;

    public Coco() {
        x = 9*32+3;
        y = 16*32+3;
    }

    public int getX() {
        return x;
    }
    public void setX(int x) {
        this.x = x;
    }
    public int getY() {
        return y;
    }
    public void setY(int y) {
        this.y = y;
    }

}
